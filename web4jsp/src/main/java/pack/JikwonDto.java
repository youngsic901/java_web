package pack;

public class JikwonDto {
    private String jikwonno, jikwonname, jikwonjik, jikwongen;
    private int jikwonpay;

    public String getJikwonno() {
        return jikwonno;
    }

    public void setJikwonno(String jikwonno) {
        this.jikwonno = jikwonno;
    }

    public String getJikwonname() {
        return jikwonname;
    }

    public void setJikwonname(String jikwonname) {
        this.jikwonname = jikwonname;
    }

    public String getJikwongen() {
        return jikwongen;
    }

    public void setJikwongen(String jikwongen) {
        this.jikwongen = jikwongen;
    }

    public String getJikwonjik(){
        return jikwonjik;
    }

    public void setJikwonjik(String jikwonjik){
        this.jikwonjik = jikwonjik;
    }

    public int getJikwonpay() {
        return jikwonpay;
    }

    public void setJikwonpay(int jikwonpay) {
        this.jikwonpay = jikwonpay;
    }
}
